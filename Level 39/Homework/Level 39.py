# Multiply
def multiply(a, b):
  return a * b
# Reversed strings
def solution(string):
    return string[::-1]
# Return negative
def make_negative( number ):
    return -number if number>0 else number
# Opposite number
def opposite(number):
  return number * -1
# Sum of positive
def positive_sum(arr):
    return sum(x for x in arr if x > 0)
